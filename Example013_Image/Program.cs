
void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // matrix.GetLength(0) - команда для подсчета строк в массиве, аргумент 0 = это строки
    {
        for (int j = 0; j < image.GetLength(1); j++) // matrix.GetLength(1) - команда для подсчета строк в массиве, аргумент 1 = это столбцы
        {
            // Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

